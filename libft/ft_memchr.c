#include "libft.h"

void	*ft_memchr(const void *s, int c, int n)
{
	int		i;
	char	*string;

	i = 0;
	string = (char *) s;
	while (i < n)
	{
		if ((char) string[i] == (char) c)
			return (&string[i]);
		i++;
	}
	return (0);
}
